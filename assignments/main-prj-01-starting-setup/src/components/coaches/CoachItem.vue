<template>
  <li>
    <h3>{{ fullName }}</h3>
    <h4>${{ rate }}/hour</h4>
    <div>
      <base-badge 
        v-for="area in areas"
        :key="area"
        :type="area"
        :title="area"
        >{{ area }}</base-badge>
    </div>
    <div class="actions">
      <base-button 
        v-if="!isDetailShowing" 
        link 
        :to="coachContactLink" 
        mode="outline">
        Contact
      </base-button>
      <base-button link :to="coachDetailLink">
        View Details
      </base-button>
    </div>
  </li>
</template>

<script>
export default {
  props: [
    'id', 'firstName', 'lastName', 'rate', 'areas'
  ],
  data() {
    return {
      isDetailShowing: false
    }
  },
  computed: {
    fullName() {
      return this.firstName + ' ' + this.lastName;
    },
    coachContactLink() {
      return '/coaches/' + this.id + '/contact'; //- this is one way to do it but may be too much hard coding
      //  return this.$route.path + '/' + this.id + '/contact'; 
    },
    coachDetailLink() {
      return '/coaches/' + this.id;   // ditto from above
      // return this.$route.path + '/' + this.id; 
    }
  }
}
</script>

